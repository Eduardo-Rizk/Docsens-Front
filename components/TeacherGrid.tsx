"use client";

import { useState } from "react";
import Link from "next/link";
import { BadgeCheck, Calendar, Clock, ArrowUpRight, Users } from "lucide-react";
import { formatLongDate, formatPrice, formatTime } from "@/lib/format";
import type { ClassEvent } from "@/lib/domain";

export type TeacherCardData = {
  id: string;
  photo: string;
  headline: string;
  bio: string;
  isVerified: boolean;
  userName: string;
  avatarColor: string;
  avatarTextColor: string;
  accentHex: string;
  nextEvent: ClassEvent | undefined;
  events: ClassEvent[];
  openCount: number;
};

function isSoldOut(event: ClassEvent) {
  return event.soldSeats >= event.capacity;
}

// ─── Filter Bar ───────────────────────────────────────────────────────────────

function FilterBar({
  teachers,
  selected,
  onSelect,
}: {
  teachers: TeacherCardData[];
  selected: string | null;
  onSelect: (id: string | null) => void;
}) {
  if (teachers.length <= 1) return null;

  return (
    <div className="flex flex-wrap items-center gap-2">
      <span className="mr-1 text-[10px] font-bold uppercase tracking-[0.16em] text-muted-foreground/60">
        Filtrar por
      </span>

      <button
        onClick={() => onSelect(null)}
        className={`relative rounded-sm border px-3.5 py-1.5 text-xs font-semibold tracking-wide transition-all duration-150 ${
          selected === null
            ? "border-brand-accent bg-brand-accent/10 text-brand-accent"
            : "border-border text-muted-foreground hover:border-zinc-600 hover:text-foreground"
        }`}
      >
        Todos
      </button>

      {teachers.map((t) => (
        <button
          key={t.id}
          onClick={() => onSelect(selected === t.id ? null : t.id)}
          className={`relative rounded-sm border px-3.5 py-1.5 text-xs font-semibold tracking-wide transition-all duration-150 ${
            selected === t.id
              ? "border-brand-accent bg-brand-accent/10 text-brand-accent"
              : "border-border text-muted-foreground hover:border-zinc-600 hover:text-foreground"
          }`}
        >
          {t.userName}
        </button>
      ))}
    </div>
  );
}

// ─── Teacher Card ─────────────────────────────────────────────────────────────

function TeacherCard({
  teacher,
  institutionId,
}: {
  teacher: TeacherCardData;
  institutionId: string;
}) {
  const next = teacher.nextEvent;
  const profileHref = `/instituicoes/${institutionId}/professores/${teacher.id}`;

  return (
    <article className="group relative flex flex-col overflow-hidden rounded-sm border border-border bg-surface transition-all duration-200 hover:border-zinc-700 hover:bg-zinc-900/60">
      {/* Accent top bar */}
      <div
        className="h-[2px] w-full shrink-0"
        style={{ background: `linear-gradient(90deg, ${teacher.accentHex}80, transparent)` }}
      />

      <div className="flex flex-1 flex-col gap-5 p-6">
        {/* ── Teacher identity (links to profile) ─── */}
        <Link href={profileHref} className="flex items-start gap-4">
          <div
            className={`flex h-14 w-14 shrink-0 items-center justify-center rounded-sm border text-sm font-bold ${teacher.avatarColor}`}
          >
            {teacher.photo}
          </div>

          <div className="min-w-0 flex-1 pt-0.5">
            <div className="flex items-center gap-1.5">
              <h3 className="truncate font-display text-xl leading-tight text-foreground group-hover:text-brand-accent transition-colors duration-150">
                {teacher.userName}
              </h3>
              {teacher.isVerified && (
                <BadgeCheck size={16} className="shrink-0 text-brand-accent" />
              )}
            </div>

            <p
              className="mt-0.5 text-xs font-medium leading-snug"
              style={{ color: teacher.accentHex }}
            >
              {teacher.headline}
            </p>

            <p className="mt-1.5 text-[11px] text-muted-foreground">
              {teacher.events.length} aula{teacher.events.length !== 1 ? "s" : ""} publicada
              {teacher.events.length !== 1 ? "s" : ""}
              {teacher.openCount > 0 && (
                <span className="ml-1.5 text-emerald-400">
                  · {teacher.openCount} com vaga
                </span>
              )}
            </p>
          </div>
        </Link>

        {/* ── Bio ─────────────────────────────────── */}
        <p className="line-clamp-2 text-sm leading-relaxed text-muted-foreground">
          {teacher.bio}
        </p>

        {/* ── Next class preview ───────────────────── */}
        {next ? (
          <div className="space-y-3 rounded-sm border border-border/60 bg-background/50 p-4">
            <p className="text-[9px] font-bold uppercase tracking-[0.18em] text-muted-foreground/60">
              Próxima aula
            </p>

            <div className="space-y-1">
              <p className="line-clamp-1 text-sm font-semibold leading-snug text-foreground">
                {next.title}
              </p>
              <p className="line-clamp-2 text-xs leading-relaxed text-muted-foreground">
                {next.description}
              </p>
            </div>

            <div className="flex items-center gap-4 text-xs text-muted-foreground">
              <span className="flex items-center gap-1.5">
                <Calendar size={11} />
                {formatLongDate(next.startsAt)}
              </span>
              <span className="flex items-center gap-1.5">
                <Clock size={11} />
                {formatTime(next.startsAt)}
              </span>
            </div>

            <div className="flex items-center justify-between pt-0.5">
              <span className="font-display text-lg text-foreground">
                {formatPrice(next.priceCents)}
              </span>
              {isSoldOut(next) ? (
                <span className="text-[10px] font-bold uppercase tracking-wider text-amber-400">
                  Esgotado
                </span>
              ) : (
                <span className="flex items-center gap-1 text-[10px] font-medium text-emerald-400">
                  <Users size={10} />
                  {next.capacity - next.soldSeats} vaga{next.capacity - next.soldSeats !== 1 ? "s" : ""}
                </span>
              )}
            </div>
          </div>
        ) : (
          <div className="rounded-sm border border-border/30 bg-background/20 p-3">
            <p className="text-xs text-muted-foreground/50">Sem próximas aulas agendadas</p>
          </div>
        )}

        {/* ── CTA → teacher profile ────────────────── */}
        <div className="mt-auto">
          <Link
            href={profileHref}
            className="flex items-center justify-between rounded-sm border border-brand-accent/30 bg-brand-accent/5 px-4 py-2.5 text-xs font-semibold uppercase tracking-wider text-brand-accent transition-colors duration-150 hover:bg-brand-accent/15"
          >
            <span>Ver todas as aulas</span>
            <ArrowUpRight size={12} className="ml-2 shrink-0" />
          </Link>
        </div>
      </div>
    </article>
  );
}

// ─── Root Export ─────────────────────────────────────────────────────────────

export function TeacherGrid({
  teachers,
  institutionId,
}: {
  teachers: TeacherCardData[];
  institutionId: string;
}) {
  const [selected, setSelected] = useState<string | null>(null);

  const visible = selected ? teachers.filter((t) => t.id === selected) : teachers;

  return (
    <div className="space-y-6">
      <FilterBar teachers={teachers} selected={selected} onSelect={setSelected} />

      <div className="grid gap-4 sm:grid-cols-2 lg:grid-cols-3">
        {visible.map((t) => (
          <TeacherCard
            key={t.id}
            teacher={t}
            institutionId={institutionId}
          />
        ))}
      </div>
    </div>
  );
}
